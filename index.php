							<p>Alejandro Belkin nos ofrece en este libro un detallado análisis sobre el proceso de emergencia del sindicalismo revolucionario y de su conquista de la hegemonia dentro del movimiento obrero, retratando su labor militante y los principales debates que le singularizaron dentro de  las organizaciones  obreras de principio del siglo XX. Este esfuerzo de comprension es esencial para deshilvanar la formacion ideológica del movimiento obrero argentino,  comprensión que en el caso del sindicalismo revolucionario la histografia desfigura o desconoce, a pesar de haber sido un de sus principales corrientes ideológicas. Para realizar esta investigación, el autor se ha apoyado en una amplia y completa revision de fuentes, de los principales periodicos de las centrales y gremios liderados por sus militantes, de libros de actas de las centrales obreras así como periódicos de las otras corrientes del movimiento social, bibliotecas y archivos, contrastadas con informaciones de diarios nacionales y análisis de fuentes públicas. El trabajo aporta luz sobre las</p>
							<p class="segundo">distintas fases del desarrollo de la “ nueva corriente ” desde sus orígenes dentro del Partido Socialista y suposterior ruptura, su papel en la reorganizacion de las centrales obreras, en los principales conflictos laborales y, en particular, revaloriza su incidencia en la huelaga de la Semana Roja de 1909 y en los acontecomientos del Centenario que marcan un punto de inflexión en la historia del movimiento obrero de principios de siglo. Aporta ademas para mayor originalidad  una nalisis dek proceso de reconstruccion de las organizaciones obreras luego de la crisis del Centanario que desembocabanen el IX° Congreso de la FORA donde se consolida el papel dirigente del sindicalismo revolucionario dentro del movimiento sindical, papel que conservará con diferentes altibajos durante mas de una decada. En definitiva, les invito a leer el trabajo de Alejandro Belkin que constituye una porte decisivo a la historiografiaargentina y un punto de referenciaineludible para futuras investigaciones.</p>
